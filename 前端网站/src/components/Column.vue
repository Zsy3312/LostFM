            <p style="margin-left: 10%; margin-right: 10%; text-align: left;">2017年是白水第一张个人专辑《时间》发表十周年。这一年他做了名为&ldquo;『时间』十年特辑&rdquo;的两个计划。《歌选》是弹唱，而<strong>《我城》</strong>是实体录音。后者记录了白水生活的宜宾小城的街街巷巷，最后通过加入后期的个人创作，形成了一部白水凝视他所在的城市母体的一个声音样本。过去的十年里的确也是白水音乐创作中最重要的十年，确实可以纪念一下，不过原因仅此而已吗？</p>
